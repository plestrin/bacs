#define ROLc(x, y) (((x) << ((y) & 0x0000001f)) | ((x) >> (0x00000020 - ((y) & 0x0000001f))))
#define RORc(x, y) (((x) >> ((y) & 0x0000001f)) | ((x) << (0x00000020 - ((y) & 0x0000001f))))

#define mds_mult(x) (MDS[0][(x)[0]] ^ MDS[1][(x)[1]] ^ MDS[2][(x)[2]] ^ MDS[3][(x)[3]])

#define MDS_POLY 	0x169
#define RS_POLY 	0x14D

static uint32_t gf_mult(uint32_t a, uint32_t b, uint32_t p){
	uint32_t result;
	uint32_t B[2] = {0, 0};

	B[1] = b;

	result  = B[a & 0x00000001]; a >>= 1; B[1] = (p & (0x00000000 - (B[1] >> 7))) ^ (B[1] << 1);
	result ^= B[a & 0x00000001]; a >>= 1; B[1] = (p & (0x00000000 - (B[1] >> 7))) ^ (B[1] << 1);
	result ^= B[a & 0x00000001]; a >>= 1; B[1] = (p & (0x00000000 - (B[1] >> 7))) ^ (B[1] << 1);
	result ^= B[a & 0x00000001]; a >>= 1; B[1] = (p & (0x00000000 - (B[1] >> 7))) ^ (B[1] << 1);
	result ^= B[a & 0x00000001]; a >>= 1; B[1] = (p & (0x00000000 - (B[1] >> 7))) ^ (B[1] << 1);
	result ^= B[a & 0x00000001]; a >>= 1; B[1] = (p & (0x00000000 - (B[1] >> 7))) ^ (B[1] << 1);
	result ^= B[a & 0x00000001]; a >>= 1; B[1] = (p & (0x00000000 - (B[1] >> 7))) ^ (B[1] << 1);
	result ^= B[a & 0x00000001];

	return result;
}

static uint32_t h_func(uint8_t x, const uint8_t* key, uint32_t k, uint32_t offset){
	uint8_t tmp[4];

	tmp[0] = x;
	tmp[1] = x;
	tmp[2] = x;
	tmp[3] = x;

	switch (k){
		case 4 : {
			tmp[0] = SBOX[1][tmp[0]] ^ key[4 * (6 + offset) + 0];
			tmp[1] = SBOX[0][tmp[1]] ^ key[4 * (6 + offset) + 1];
			tmp[2] = SBOX[0][tmp[2]] ^ key[4 * (6 + offset) + 2];
			tmp[3] = SBOX[1][tmp[3]] ^ key[4 * (6 + offset) + 3];
		}
		case 3 : {
			tmp[0] = SBOX[1][tmp[0]] ^ key[4 * (4 + offset) + 0];
			tmp[1] = SBOX[1][tmp[1]] ^ key[4 * (4 + offset) + 1];
			tmp[2] = SBOX[0][tmp[2]] ^ key[4 * (4 + offset) + 2];
			tmp[3] = SBOX[0][tmp[3]] ^ key[4 * (4 + offset) + 3];
		}
		case 2 : {
			tmp[0] = SBOX[0][tmp[0]] ^ key[4 * (2 + offset) + 0];
			tmp[1] = SBOX[1][tmp[1]] ^ key[4 * (2 + offset) + 1];
			tmp[2] = SBOX[0][tmp[2]] ^ key[4 * (2 + offset) + 2];
			tmp[3] = SBOX[1][tmp[3]] ^ key[4 * (2 + offset) + 3];
		}
	}

	tmp[0] = SBOX[1][SBOX[0][tmp[0]] ^ key[4 * offset + 0]];
	tmp[1] = SBOX[0][SBOX[0][tmp[1]] ^ key[4 * offset + 1]];
	tmp[2] = SBOX[1][SBOX[1][tmp[2]] ^ key[4 * offset + 2]];
	tmp[3] = SBOX[0][SBOX[1][tmp[3]] ^ key[4 * offset + 3]];

	return mds_mult(tmp);
}

static void rs_mult(const uint8_t* in, uint8_t* out){
	uint32_t i;
	uint32_t j;

	for (i = 0; i < 4; i++){
		out[i] = 0;
		for (j = 0; j < 8; j++){
			out[i] ^= gf_mult(in[j], RS[i][j], RS_POLY);
		}
	}
}

void twofish128_key_init(const uint32_t* key, struct twofishKey* twofish_key){
	uint8_t 	i;
	uint32_t 	j;
	uint32_t 	a;
	uint32_t 	b;
	uint8_t 	S[4*TWOFISH_128_K];

	for (i = 0; i < TWOFISH_128_K; i++){
		rs_mult((const uint8_t*)(key + 2*i), S + 4*i);
	}

	for (i = 0; i < TWOFISH_NB_ROUND_KEY / 2; i++){
		a = h_func(2*i + 0, (const uint8_t*)key, TWOFISH_128_K, 0);
		b = h_func(2*i + 1, (const uint8_t*)key, TWOFISH_128_K, 1);
		b = ROLc(b ,8);

		twofish_key->ks[2*i + 0] = a + b;
		twofish_key->ks[2*i + 1] = ROLc(a + 2*b, 9);
	}

	for (j = 0; j < 256; j++){
		twofish_key->sbox[0][j] = MDS[0][SBOX[1][SBOX[0][SBOX[0][j] ^ S[0]] ^ S[4]]];
		twofish_key->sbox[1][j] = MDS[1][SBOX[0][SBOX[0][SBOX[1][j] ^ S[1]] ^ S[5]]];
		twofish_key->sbox[2][j] = MDS[2][SBOX[1][SBOX[1][SBOX[0][j] ^ S[2]] ^ S[6]]];
		twofish_key->sbox[3][j] = MDS[3][SBOX[0][SBOX[1][SBOX[1][j] ^ S[3]] ^ S[7]]];
	}
}

