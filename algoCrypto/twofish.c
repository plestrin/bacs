void twofish_encrypt(const uint32_t* input, const struct twofishKey* twofish_key, uint32_t* output){
	uint32_t a;
	uint32_t b;
	uint32_t c;
	uint32_t d;
	uint32_t r;

	a = input[0] ^ twofish_key->ks[0];
	b = input[1] ^ twofish_key->ks[1];
	c = input[2] ^ twofish_key->ks[2];
	d = input[3] ^ twofish_key->ks[3];

	for (r = 0; r < TWOFISH_NB_ROUND; r++){
		uint32_t tmp1;
		uint32_t tmp2;

		tmp1 = twofish_key->sbox[0][(a >> 0 ) & 0x000000ff] ^ twofish_key->sbox[1][(a >> 8 ) & 0x000000ff] ^ twofish_key->sbox[2][(a >> 16) & 0x000000ff] ^ twofish_key->sbox[3][(a >> 24) & 0x000000ff];
		tmp2 = twofish_key->sbox[1][(b >> 0 ) & 0x000000ff] ^ twofish_key->sbox[2][(b >> 8 ) & 0x000000ff] ^ twofish_key->sbox[3][(b >> 16) & 0x000000ff] ^ twofish_key->sbox[0][(b >> 24) & 0x000000ff];

		c = RORc((tmp1 + tmp2 + twofish_key->ks[8 + 4*r + 0]) ^ c, 1);
		d = ROLc(d, 1) ^ (2*tmp2 + tmp1 + twofish_key->ks[8 + 4*r + 1]);

		tmp1 = twofish_key->sbox[0][(c >> 0 ) & 0x000000ff] ^ twofish_key->sbox[1][(c >> 8 ) & 0x000000ff] ^ twofish_key->sbox[2][(c >> 16) & 0x000000ff] ^ twofish_key->sbox[3][(c >> 24) & 0x000000ff];
		tmp2 = twofish_key->sbox[1][(d >> 0 ) & 0x000000ff] ^ twofish_key->sbox[2][(d >> 8 ) & 0x000000ff] ^ twofish_key->sbox[3][(d >> 16) & 0x000000ff] ^ twofish_key->sbox[0][(d >> 24) & 0x000000ff];

		a = RORc((tmp1 + tmp2 + twofish_key->ks[8 + 4*r + 2]) ^ a, 1);
		b = ROLc(b, 1) ^ (2*tmp2 + tmp1 + twofish_key->ks[8 + 4*r + 3]);
	}

	output[0] = c ^ twofish_key->ks[4];
	output[1] = d ^ twofish_key->ks[5];
	output[2] = a ^ twofish_key->ks[6];
	output[3] = b ^ twofish_key->ks[7];
}

void twofish_decrypt(const uint32_t* input, const struct twofishKey* twofish_key, uint32_t* output){
	uint32_t a;
	uint32_t b;
	uint32_t c;
	uint32_t d;
	uint32_t r;

	a = input[2] ^ twofish_key->ks[6];
	b = input[3] ^ twofish_key->ks[7];
	c = input[0] ^ twofish_key->ks[4];
	d = input[1] ^ twofish_key->ks[5];

	for (r = TWOFISH_NB_ROUND; r > 0; r--){
		uint32_t tmp1;
		uint32_t tmp2;

		tmp1 = twofish_key->sbox[0][(c >> 0 ) & 0x000000ff] ^ twofish_key->sbox[1][(c >> 8 ) & 0x000000ff] ^ twofish_key->sbox[2][(c >> 16) & 0x000000ff] ^ twofish_key->sbox[3][(c >> 24) & 0x000000ff];
		tmp2 = twofish_key->sbox[1][(d >> 0 ) & 0x000000ff] ^ twofish_key->sbox[2][(d >> 8 ) & 0x000000ff] ^ twofish_key->sbox[3][(d >> 16) & 0x000000ff] ^ twofish_key->sbox[0][(d >> 24) & 0x000000ff];


		a = ROLc(a, 1) ^ (tmp1 + tmp2 + twofish_key->ks[8 + 4*(r - 1) + 2]);
		b = RORc((2*tmp2 + tmp1 + twofish_key->ks[8 + 4*(r - 1) + 3]) ^ b, 1);

		tmp1 = twofish_key->sbox[0][(a >> 0 ) & 0x000000ff] ^ twofish_key->sbox[1][(a >> 8 ) & 0x000000ff] ^ twofish_key->sbox[2][(a >> 16) & 0x000000ff] ^ twofish_key->sbox[3][(a >> 24) & 0x000000ff];
		tmp2 = twofish_key->sbox[1][(b >> 0 ) & 0x000000ff] ^ twofish_key->sbox[2][(b >> 8 ) & 0x000000ff] ^ twofish_key->sbox[3][(b >> 16) & 0x000000ff] ^ twofish_key->sbox[0][(b >> 24) & 0x000000ff];

		c = ROLc(c, 1) ^ (tmp1 + tmp2 + twofish_key->ks[8 + 4*(r - 1) + 0]);
		d = RORc((2*tmp2 + tmp1 + twofish_key->ks[8 + 4*(r - 1) + 1]) ^ d, 1);
	}

	output[0] = a ^ twofish_key->ks[0];
	output[1] = b ^ twofish_key->ks[1];
	output[2] = c ^ twofish_key->ks[2];
	output[3] = d ^ twofish_key->ks[3];
}
